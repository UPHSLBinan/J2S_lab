            There’s no escaping the fact that 2022 was challenging for some mobile game studios. 
            After a decade of phenomenal growth, factors including a global recession, post-pandemic 
            changes to consumer behavior and spending, rising advertising costs, and user acquisition 
            challenges in the aftermath of Apple’s Tracking Transparency (ATT) policies caused the 
            mobile games market to decline for the very first time. <br><br>

            As a result, we saw plenty of interesting trends emerge, many of which are set to continue 
            through 2023. These include some of the mobile game trends and predictions that we made for 
            2022, such as AAA and midcore games starting to take the place of casual games in the 
            top-grossing charts and the hybridization of mobile game genres through the addition of 
            minigames (we never thought we’d see a TCG game inside of Genshin Impact but here we are!).<br><br>

            Some trends didn’t stay around for long, though. The value of cryptocurrencies has plummeted massively, 
            meaning blockchain mobile games haven’t experienced the level of growth they were expected to. And while 
            2022 seemed to be the year of experimental gacha growth in mobile games, looming legislation on gachas 
            and loot boxes means some mobile games are ditching gachas entirely in favor of more transparent 
            monetization methods such as battle passes – Mario Kart Tour and Brawl Stars are two of the latest 
            examples. 
